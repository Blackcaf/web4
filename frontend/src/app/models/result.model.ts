/**
 * Модель запроса для проверки попадания точки в область.
 */
export interface PointRequest {
  /** Координата X точки (допустимый диапазон: [-5, 3]) */
  x: number;
  /** Координата Y точки (допустимый диапазон: [-3, 5]) */
  y: number;
  /** Радиус области (допустимый диапазон: (0, 3]) */
  r: number;
}

/**
 * Модель результата проверки попадания точки.
 * Содержит все данные о проверке, включая время выполнения.
 */
export interface Result {
  /** Уникальный идентификатор результата в БД */
  id: number;
  /** Координата X проверенной точки */
  x: number;
  /** Координата Y проверенной точки */
  y: number;
  /** Радиус использованной области */
  r: number;
  /** Результат проверки: true - попадание, false - промах */
  hit: boolean;
  /** Время проверки в формате ISO 8601 */
  timestamp: string;
  /** Время выполнения проверки в микросекундах */
  executionTime: string;
}

